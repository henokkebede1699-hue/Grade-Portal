// --- CORE FUNCTIONALITY ---
function checkScores() {
    const selectedSubject = document.getElementById('subjectSelect').value;
    const inputName = document.getElementById('studentName').value.trim();
    const inputId = document.getElementById('studentId').value.trim();
    
    const errorDiv = document.getElementById('errorMessage');
    const resultsCard = document.getElementById('resultsCard');
    const searchForm = document.getElementById('searchForm');
    
    errorDiv.innerText = "";
    resultsCard.classList.add('hidden');

    if (!selectedSubject) {
        errorDiv.innerText = "Please select a subject from the dropdown menu.";
        return;
    }

    const classList = database[selectedSubject] || [];
    const foundStudent = classList.find(student => 
        student.name.toLowerCase().replace(/\s+/g, '') === inputName.toLowerCase().replace(/\s+/g, '') && 
        student.id.toUpperCase().replace(/\s+/g, '') === inputId.toUpperCase().replace(/\s+/g, '')
    );

    if (foundStudent) {
        const totalScore = foundStudent.assignments + foundStudent.project + foundStudent.finalExam;
        
        const statusEl = document.getElementById('resStatus');
        if (totalScore >= 50) {
            statusEl.innerText = "PASSED";
            statusEl.className = "status-badge pass";
        } else {
            statusEl.innerText = "FAILED";
            statusEl.className = "status-badge fail";
        }

        const courseDropdown = document.getElementById('subjectSelect');
        const courseName = courseDropdown.options[courseDropdown.selectedIndex].text;

        document.getElementById('resCourse').innerText = courseName;
        document.getElementById('resName').innerText = foundStudent.name;
        document.getElementById('resId').innerText = foundStudent.id;

        document.getElementById('gradeAssignments').innerText = foundStudent.assignments + " / 20";
        document.getElementById('gradeProject').innerText = foundStudent.project + " / 30";
        document.getElementById('gradeFinal').innerText = foundStudent.finalExam + " / 50";
        document.getElementById('gradeTotal').innerText = totalScore + " / 100";

        searchForm.classList.add('hidden');
        resultsCard.classList.remove('hidden');
    } else {
        errorDiv.innerText = "Records not found in this subject. Please check your spelling and ID formatting.";
    }
}

function resetPortal() {
    document.getElementById('resultsCard').classList.add('hidden');
    document.getElementById('searchForm').classList.remove('hidden');
    
    document.getElementById('subjectSelect').value = "";
    document.getElementById('studentName').value = "";
    document.getElementById('studentId').value = "";
    document.getElementById('errorMessage').innerText = "";
}
